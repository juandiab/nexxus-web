export function splitBoldSegments(text) {
  return String(text)
    .split(/(\*\*[^*]+\*\*)/g)
    .filter(Boolean)
    .map((part) => {
      if (part.startsWith('**') && part.endsWith('**')) {
        return { bold: true, text: part.slice(2, -2) }
      }
      return { bold: false, text: part }
    })
}
